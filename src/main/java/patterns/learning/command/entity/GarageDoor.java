package patterns.learning.command.entity;

public class GarageDoor {

    public void up() {
        System.out.println("door up");
    }

    public void down() {
        System.out.println("door down");
    }

    public void stop() {
        System.out.println("door stop");
    }

    public void lightOn() {
        System.out.println("light on in garage");
    }

    public void lightOff() {
        System.out.println("light off in garage");
    }

}
